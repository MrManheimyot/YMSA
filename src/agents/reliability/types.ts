export interface ReliabilityAgentStats {
  totalAssessments: number;
  avgTrustScore: number;
  contradictionsDetected: number;
  staleDataBlocked: number;
  highTrustApprovals: number;
  lowTrustRejections: number;
  sourceAccuracyMap: Record<string, number>;  // sourceId → rolling accuracy
  lastResetAt: number;
}
